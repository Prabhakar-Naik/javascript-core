var num1 = 20;
var num2 = 34;

var result = num1 > num2;
console.log(num1, " > ", num2, result);

result = num1 >= num2;
console.log(num1, " >= ", num2, result);

result = num1 < num2;
console.log(num1, " < ", num2, result);

result = num1 <= num2;
console.log(num1, " <= ", num2, result);

result = num1 === num2;
console.log(num1, " === ", num2, result);

result = num1 != num2;
console.log(num1, " != ", num2, result);

result = num1 !== num2;
console.log(num1, " !== ", num2, result);

result = num1 > num2 && num2 < num1;
console.log(num1 > num2, "&&", num2 < num1, result);
